PROGRAM PRINT
    IMPLICIT NONE
    ! VARIABLE DECLARATIONS
    INTEGER::VAR_1=4
    REAL::VAR_2=6.2324
    REAL::VAR_3=45.2324
    REAL::VAR_4=7.2324
    REAL::VAR_5=9.2324
    ! PRINT REGULARLY
    PRINT *, VAR_1, VAR_2, VAR_3, VAR_4, VAR_5
    ! PRINT WITH FORMAT IDENTIFIER
    PRINT "(I1, 4F8.4)", VAR_1, VAR_2 , VAR_3, VAR_4, VAR_5
    PRINT 1, VAR_1, VAR_2 , VAR_3, VAR_4, VAR_5
1   FORMAT (I1, /, 4F8.4)
END PROGRAM PRINT
